It has been, unscrupulously, an appalling tour for India, by all standards. One can claim that the team emerged victorious in half the matches on tour, but with a clear introspection on their performances, it has been NZ’s repeated bollix, especially in the crunch overs. And you don’t say this very often, but one player who has failed to put his hand up is Captain King Kohli himself.


It was not only his numbers which were new flanged, but also his mental approach toward the tour. The skipper, who is known for his belligerence on the field, went to NZ with a rather uncustomary attitude. From being overridden in the WC semi by the kiwis to deeming them as “so nice” and chatting with Kane Williamson on life, it seems as if he was looking at the tour with a tinge of trifle, given that impact players like Boult and Ferguson were missing early. on.


Notwithstanding the 45(32) in the first game on tour, his performances have routinely declined, even to an extent of meager single digits in the tests. With his knowledge in mind, it is quite difficult to believe that he was the man who aced the moving ball and looked a comprehensive player against the opposition he had scuffled against 6 years ago.


And Virat, who is well known from his ability to bounce back from lackluster outings, displayed no inclination to ameliorate. He made a valiant statement in the 2018 England tour after a scanty 2014 tour. He brought the Australian bowlers to his feet in 2014 with the twin Adelaide tons after a humbling series in 2012. He conquered South Africa after a see-saw 2013 series. Given his history of rising to the occasion, irrespective of conditions, he was fully antipodal on this tour.


If he has shown resolution anywhere, it has been his answers to the media. Vocalizing that ODI’s are not too relevant speaks volumes of the mental atmosphere in the dressing room. This was a statement even Head Coach Shastri would make later on after a thrashing in the first test. With this on hand, it remains a mystery as to why the Australia home series resurgence was greatly commemorated. Knowing that the team has eliminated one format from their preparations, it portrays that the players may not be giving their 100% on the field and “being the best versions of themselves”, which is a mantra the captain lives on.


It was also astonishing to see his counterstatement that he was a no-show. With a careful reading between the lines, it is relatively simple to make out that he was merely concealing by saying that he was “batting well”. With all these years, this is certainly not the Virat Kohli we know. IF there is a dip in form, he will always own up, pull up his socks, and return with a bang.


For Virat Kohli, rage and determination have always driven him to success. Somehow, that piece of him was vacant. In the 2019 series against WI, it was the repeated sledging from the West Indians which got him his desire to prove himself with a match-winning 94. In the decider game, it was his constant self motivation that got him to score a quickfire 70. It was his drive which got him to win Man of Series against Australia. The humble attitude from the kiwis just weeded out that aspect from him. Even with the tour ending, while he had claimed collective responsibility of the batsmen, not once did he recognize his batting drop.


Only time will tell if he can once again re-establish himself, but the team is in dire need of him. India will definitely need a golden run from him if they will have any hope of winning the WT20. Till then, I can only wish that he takes the world by surprise in the coming SA series and the IPL.</p>
