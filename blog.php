                    <h2>What is Badla Financing and why was it banned?
                        <br>
                        <small></small>
                    </h2>
                    <p>Amid a pandemic, as we all are segregated, I am going to rewind the clocks to an old Indian stock trading system. Back in the day, when firm ground-rules on stock trading were not established, investors would capitalize on the recourse to carry forward their stock purchase transactions.

Before any further discussion, it is essential to explain what “carrying forward ” means. Prior to any formal stock transaction , the buyer and seller, through recognized brokers, would simply agree on a deal and propose a settlement date among themselves. The deal would typically consist of how many shares to be sold/bought and the price that must be paid on the settlement date. Only on the settlement date, the formal transaction would proceed. Now, carrying forward comes into play when either party decides to postpone the transactions beyond the settlement date. Earlier, in the Indian stock market, such practices were deemed acceptable, but today, there are stern limits on carrying forward transactions.

Carrying forward transactions poses a high dose of uncertainty and financial risk, and subsequently, it was superseded with a concept known as a futures contract. Here, both the buyer and seller will formally agree to transact on a particular day, with a specific amount to escape the up-shots of a volatile market.

So, badla financing, in essence, is a stock trading system wherein either party decides to carry forward per the current market trend. And depending on whether the stock market index is increasing(AKA forwardation or contango), or decreasing(AKA backwardation), the scenario will influence the decision making of either the buyer or seller. It is formally known as ‘badla’ if the buyer decides to postpone the transactions or ‘undha badla’, or reverse badla if the seller postpones the stock transaction. In both these cases, the stock market will act as a median to determine a suitable interest rate given the demand for the stocks.

Let us consider the following scenario wherein an investor(Person A ) is involved in the stock purchases of a company known as TM-MOTORS

At present, let us assume that TM-MOTORS is selling shares for 2000 rupees each and Person A considers that a massive markdown and speculates the price to rise shortly. However, currently, A lacks the money to purchase the shares of TM-MOTORS. It is in these scenarios where eager badla financers can make hay. Let us say that there is a badla financer Y, who has enough money to purchase the stocks. A can request Y to purchase the shares on A’s behalf and carry forward the payment for the shares up to the point where A can afford the shares. Nonetheless, A will be required to pay interest to Y for the delay, which would be determined via the stock exchange using the forces of demand and supply for that particular share. Fundamentally, this outcome is a concession for both A and Y. A will not need to buy the stock at its increased future price, rather just pay an interest, and Y would gain the interest amount itself. The badla financers can either be independent or a contact via the stockbroker.

The opposite of this is known as ‘undha-badla’, whereas you may have already guessed, the market is in backwardation, meaning the future stock prices are lower than those of the existing. Here, the seller will reckon on carrying forward the ‘selling’ of stocks as if the prices are waning, the seller will prefer to sell the stock at its diminished future price. And, as mentioned earlier, the seller will pay a badla interest for carrying forward the transaction.

Badla trading practices saw quick booms in the market as it was a comfort for all parties involved. Badla also brought in high volumes of stock trading in the early 1990s.

If Badla trading was a win-win, why did the government ban it???

Badla trading has a see-sawed past in the Indian Stock market. It was first banned in 1993 by the SEBI due to its anomalies, partially brought back in 1995, and finally banned in 2001. So, what caused this skepticism in the SEBI?

THE ANSWER: The Harshad Mehta Scam

In 1992, Harshad Mehta(An Indian stockbroker), made the front page for one of the biggest scams of the Bombay Stock Exchange(BSE).

Back in the 90s, all investment banks were required to buy and reserve some government securities. So, if a bank’s capital was down-swinging, the bank would sell the government securities and re-buy them with a bit of interest. Harshad Mehta, a clever intermediate broker, would position himself in the buying and selling of government bonds. Whenever a bank needed to purchase these bonds, he would take money from the bank and in return, take some time to find a suitable seller. He immensely carried forward these transactions and just used the money to invest in stocks. Stock prices skyrocketed to greater limits and Harshad repeated this with almost all the big commercial banks in India. In due course, when the banks clamored for their money, he would just find another bank who was ready to give their money, and this recursively continued.

The more money he collected, the more jolts he induced in the stock prices. He even went to the extent of printing fake receipts to show to banks for their money! And as stock prices were soaring higher than ever, more people began investing soon.

Consequently, by nature, the stock market crashed. It was a mere repercussion of the hyper-inflation beyond the utility of the stocks themselves. Banks found out about his receipt frauds, excessive carrying forward, and his vicious borrowing cycles. Not to mention, the owner of Vijaya Bank took his life upon this news.!!! Banks lost thousands of crores and Harshad was caught in an enormous debt.

Upon the crash, the officials sat down and imposed restrictions on carrying forward transactions, and naturally, Badla was banned. It was often briefly reintroduced, just to inject some fuel into the stock market, but in 2001, it was eradicated.

The scandal has paved the way for tighter trading regulations in the Indian stock market today. On a related note, let us see that the current stock prices don’t continue plummeting by doing our bit to fight COVID19. I hope to have given you an interesting read while stranded at home, and if you liked the article, a subscription would be most kindly appreciated!!!

