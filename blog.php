                    <h2>CoronaVirus-Economic Impacts
                        <br>
                        <small>March 14th 2020</small>
                    </h2>
                    <p>It is beyond dispute that the new decade had a high-octane outset with the IRAN-USA deal and the Puerto Rico earthquake. However, in the last months, there has been a cogent impetus which has been comfortably driving the minds of mankind: Coronavirus. What is now an SOS alert can be attributed to a mere accident at an animal market in Wuhan, China. While, at this point, we are prominently looking to establish a well-built body defense system against the virus, it is salient to mention the additional ways in which the alert could be a threat to our society.

The virus has been taking a massive toll on the global economy, especially the USA and, not to mention, China. And, as a result, dependant countries have been potential victims of this recession.

While President Trump is finding the blue sky in the circumstance by saying that “People will spend their money”, it is imperative to assess the overall economic situation of the USA. The Dow Jones industrial average(A stock performance indicator) had its record biggest plummet of 1191 points, flustering investor hopes. After the overall stock market gained by a whopping 29% in 2019, investors have lost trillions of dollars due to the fall, and it is still uncertain as to how long this will continue. Although the GDP grew by 2% in the first quarter of the year, economists are looking at a dipped 1.1% growth in the coming quarter. Additionally, there have been drastic cuts in the Federal Reserve interest rates, showing us that the USA government is treating this situation no differently than the 2007 recession.

Notwithstanding the mega-drop, President Trump was still not pleased with the cuts, saying that the rates should have dropped more, but that’s a topic for another post.

In parallel, the epidemic also rubs salt into the wounds of the UK’s economic setbacks since its exit from the EU. The island already had a low-key growth rate of 1.2%, much below its average. And to top it up, the consistently dipping government bond returns have caused the Band of New England to slash the interest rate to 0.25%. Yes, such monetary policies are quite instinctive, but a more effective way to boost the economy in the long term would be for the government to cut taxes and increase its expenditure and the real GDP.

British firms have also been apprised that at least 20% of their labor force could be out sick. With already more than 350 confirmed cases in the country, it is estimated that a total of 6 million workers would not be present at work, not including those taking off to care for the ill. It goes without saying, this is a massive blow for an already stagnant economy.

On the other end of the world, the coronavirus has caused slow production in China, affecting countries like India, which has taken a 348m dollar trade impact. In a way, the virus is the perfect indicator of how dependant the world is on China’s product manufacturing. Looking at the massive trade hits in the chemical and textile industries in India, it is needless to say that the EU and the USA ‘s 15.6 billion and 5.8 billion dollar hit respectively is a debacle.

There has been a visible 2% fall in production rates in China. In fact, due to these receding economies, Oyo Hotels, a hotel company based in India, has made a bold decision to fire 6000 full-time employees in China. , Hotel occupancy rates have been in freefall, leaving at least 8 out of 10 rooms unoccupied. The hotel industries incurred gigantic losses, especially in the time of the recent Chinese new year, which is a peak travel time.

Specifically, who else loses out?

Unquestionably, barring the general world economy, particular professions will come under fire, especially in countries with cheap labor.

Maids and cooks will certainly be turned back as a mere precaution. With the case count on the rise, it will increasingly become arduous to track down where the maids have gone and explore potential ends for the virus spread. As a result, people in such professions will have to resort to vicious cycles of borrowing, and with a very meager income, worsen their financial debt.

To add on, a myriad of social buildings and schools have been shut down. So, it is not only housemaids who are losing out, but also those the majority of them who are employed in these places.

Successively, those involved in transport will face unemployment. Bus conductors, drivers, will not have an income inflow for these weeks as the public would be against traveling with a crowd.

Needless to say, the tourist industry is taking immense damage. Looking at Goa, India, the Goan people feed off from tourism and mining, which are primary sectors there. Mining has already been banned in India due to the dumping of low-grade ores and the tourism hit is the final nail in the coffin for Goans. Arbitrarily, the Goan government is currently looking to attain back some mining rights to keep the mining dependents running.

And an immediate effect, as we have already started seeing now, would be economic collapses due to less disposable income and subsequent expenditure.

So many lost, so does anyone benefit???

The answer is Yes. Apart from these FMCG hand sanitizers, with any economic overturn, a collection of industries will always make hay. In this case, we need to assess how we will continue to live and learn, given that many of us are practically on house arrest. With schools and universities being shut down, this is the perfect occasion for online learning tools to get off the mark. Coursera has made around 3000 courses free to increase customer demand. As educational institutes have started creating online classes, software like Microsoft teams and outlook will look to capitalize on this window

Not to mention, time at home awards us more TV and Netflix time!!!. Because work has taken a backseat, and sports matches have been shut down, TV channels and companies like Amazon Prime, will have far more viewership. In fact, Netflix has introduced a google chrome extension called Netflix Party, where people can virtually form groups and watch movies.

The way forward

The virus has its set of merits and drawbacks on our society, but undoubtedly more of the latter. Economic growth is coming to a standstill and at this point, responsibly taking precautions is what will pull us out of this hole. With fatality out of the picture, all that remains is the prevention of its spread, and we can do this through thoughtful decisions and less panic. The faster this message reaches the globe, the faster we can start traveling to our favorite destinations and start watching our treasured sports matches.

